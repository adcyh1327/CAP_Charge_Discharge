uint16_t Get_rtuCrc16(uint8_t *puchMsg,uint16_t usDataLen) 
{ 
    uint8_t uchCRCHi=0xFF; 		//∏ﬂCRC ◊÷Ω⁄≥ı ºªØ 
    uint8_t uchCRCLo=0xFF; 		//µÕCRC ◊÷Ω⁄≥ı ºªØ  
    uint32_t uIndex; 	 		//CRC —≠ª∑÷–µƒÀ˜“˝ 
	while(usDataLen--) 	//¥´ ‰œ˚œ¢ª∫≥Â«¯ 
	{ 
		uIndex=uchCRCHi^*puchMsg++; //º∆À„CRC  
		uchCRCHi=uchCRCLo^auchCRC16_Hi[uIndex]; 
		uchCRCLo=auchCRC16_Lo[uIndex]; 
	} 
	return (uchCRCHi<<8|uchCRCLo); 
}

